<div class="resume">
  <h3>Description</h3>
  $A The macro bunny_errno is a simple wrapper around the system errno.
  Any errno valid value are valid for bunny_errno, any t_bunny_errno
  value are valid for bunny_errno. bunny_errno can be send to bunny_strerror
  to display a human readable error explaination.
